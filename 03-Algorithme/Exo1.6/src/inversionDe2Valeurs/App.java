package inversionDe2Valeurs;

import java.util.Scanner;

public class App {

	public static void main(String[] args) {
		
		
		int nombreA;
		int nombreB;
		Scanner sc;
		
		sc = new Scanner(System.in);
		
		System.out.println("Veuillez saisir votre nombre");
		
		nombreA = sc.nextInt();
		
		System.out.println("Veuillez entrer votre nombre");
		
		nombreB = sc.nextInt();
		
		System.out.println("");
		
		System.out.println("nombreB = " + nombreB);
		
		System.out.println("");
		
		System.out.println("nombreA = " +nombreA);

		System.out.println("");
		
		System.out.println("nombreA = " +nombreA);
		
		System.out.println("");
		
		System.out.println("nombreB = " +nombreB);
		
		System.out.println("");
		
		
		
		sc.close();
	
	}

}
