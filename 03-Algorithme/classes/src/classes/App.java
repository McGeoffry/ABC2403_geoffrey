package classes;

public class App {

	public static void main(String[] args) {
	
		
		int a;
		int b;
		int c;
		// Boolean resultat
		
		
		a = 1; // a = 1;
		b = a++; // b = a + 1;
		c = ++a; // 
		System.out.println("a = " + a);
		System.out.println("b = " + b);
		System.out.println("c = " + c);
		
		
		double x = 10;
		double y = 3;
		
		System.out.println(x % y); // % permet d'obtenir le restant d'une division et si divisible = 0 : modulo
		
		
	}
	
	
}
